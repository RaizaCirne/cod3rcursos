let a = 1; 

// Formas de acrescentar um ao valor de uma variável, nesse caso, a variável a. 
a = a + 1; // operador binário - infix (operador entre dos dois operandos) 
a += 1;   // Atribuição aditiva - operador binário  - infix (operador entre dos dois operandos)
a++;     // operador unário - incremento -  postfix (operador depois do operando)
++a;    // operador unário  - incremento - prefix (operador antes do operando)

a--;    // operador unário - decremento - postfix (operador depois do operando)
--a;   // operador unário - decremento - prefix (operador antes do operando)

console.log(a);
